package Practise_3;

public class Converter {
    double amount;
    public Converter() {
        this.amount = amount;
    }


    public double convertRUBtoUSD(double amount) {return amount*0.0108;}
    public double convertRUBtoCNY(double amount) {return amount*0.14;}

    public double convertUSDtoRUB(double amount) {return amount*92.58;}
    public double convertUSDtoCNY(double amount) {return amount*7.05;}

    public double convertCNYtoRUB(double amount) {return amount*13.04;}
    public double convertCNYtoUSD(double amount) {return amount*0.14;}







}
